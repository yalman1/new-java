package loops;

public class lowerCase {
    public static void main(String[] args) {
        for (int i=97; i<=122; i++){
            System.out.println("lower case=\t"+(char)i+"\t\t="+(int)i);
        }
        for (int i=97; i<=122; i++){
            System.out.println((char)i);  //only letter
        }

    }
}
